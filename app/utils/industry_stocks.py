def get_stocks_for_industry(industry: str) -> list[tuple[str, str]]:
    """Return representative stocks for a given industry name.

    Uses exact match → case-insensitive → alias resolution.
    Returns empty list if no match found.
    """
    # Direct match (normalized)
    if industry in _NORMALIZED:
        return _NORMALIZED[industry]

    # Try alias resolution
    canonical = _ALIASES.get(industry) or _ALIASES.get(industry.lower())
    if canonical and canonical in _NORMALIZED:
        return _NORMALIZED[canonical]

    # Fuzzy: find the longest-matching key (best match)
    industry_lower = industry.lower()
    best_match = None
    best_len = 0
    for key in INDUSTRY_STOCKS:
        if key.lower() in industry_lower or industry_lower in key.lower():
            if len(key) > best_len:
                best_match = key
                best_len = len(key)
    if best_match:
        return INDUSTRY_STOCKS[best_match]

    # Tier 4: Broad category fallback via keyword scanning
    for keyword, candidates in _BROAD_KEYWORDS.items():
        if keyword in industry:
            for candidate in candidates:
                if candidate in _NORMALIZED:
                    return _NORMALIZED[candidate]

    return []


def get_stocks_for_industries(industries: list[str]) -> dict[str, list[tuple[str, str]]]:
    """Map a list of industry strings to their representative stocks.

    Returns {industry: [(symbol, name), ...]} for matched industries only.
    """
    result = {}
    seen_stocks = set()
    for ind in industries:
        stocks = get_stocks_for_industry(ind)
        if stocks:
            # Deduplicate stocks across industries
            unique = [s for s in stocks if s[0] not in seen_stocks]
            if unique:
                result[ind] = unique
                for s in unique:
                    seen_stocks.add(s[0])
    return result
